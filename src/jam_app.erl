-module(emqtt_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
	application:start(emqtt_logger),
	emqtt_logger:info("logger started"),

	socket_server:start(),
    emqtt_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

